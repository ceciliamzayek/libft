#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dup;
	int		i;

	i = 0;
	dup = (char *)malloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (!dup)
		return (NULL);
	while (*s1)
	{
		*(dup + i) = *s1;
		s1++;
		i++;
	}
	*(dup + i) = '\0';
	return (dup);
}
