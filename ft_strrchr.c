char	*ft_strrchr(const char *s, int c)
{
	char *lst;

	lst = 0;
	while (*s)
	{
		if (*s == c)
			lst = (char *)s;
		s++;
	}
	if (*s == c)
		lst = (char *)s;
	return (lst);
}
